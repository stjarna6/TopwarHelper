constexpr double Pow10(int exp) {
    double ret = 1;
    for (int i = 0; i < exp; i++) {
        ret = ret * 10;
    }
    return ret;
}

constexpr double operator ""_T(long double val)  { return val * Pow10(3*4); }
constexpr double operator ""_aa(long double val) { return val * Pow10(3*5); }
constexpr double operator ""_bb(long double val) { return val * Pow10(3*6); }
constexpr double operator ""_cc(long double val) { return val * Pow10(3*7); }
constexpr double operator ""_dd(long double val) { return val * Pow10(3*8); }
constexpr double operator ""_ee(long double val) { return val * Pow10(3*9); }
constexpr double operator ""_ff(long double val) { return val * Pow10(3*10); }
constexpr double operator ""_gg(long double val) { return val * Pow10(3*11); }
constexpr double operator ""_hh(long double val) { return val * Pow10(3*12); }

inline QString formatNumber(double val, int precision = 2) {
    constexpr const char* Units[] = {
        "", "K", "M", "B", "T",
        "aa", "bb", "cc", "dd", "ee", "ff",
        "gg", "hh", "ii", "jj", "kk", "ll"
    };
    int exp = static_cast<int>(std::log10(val));
    int unitIdx = exp / 3;

    double t = val / std::pow(10, unitIdx * 3);
    return QString::number(t, 'f', precision) + Units[unitIdx];
}


